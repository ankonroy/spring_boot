package com.example.spring_tutorial.service;

import com.example.spring_tutorial.exception.ResourceNotFoundException;
import com.example.spring_tutorial.model.Course;
import com.example.spring_tutorial.model.Department;
import com.example.spring_tutorial.repository.CourseRepository;
import com.example.spring_tutorial.repository.DepartmentRepository;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;

import java.util.List;
import java.util.Optional;

import static org.junit.jupiter.api.Assertions.*;
import static org.mockito.ArgumentMatchers.any;
import static org.mockito.Mockito.*;

/**
 * Unit tests for CourseService.
 * Tests course CRUD operations.
 */
@ExtendWith(MockitoExtension.class)
public class CourseServiceTest {

    @Mock
    private CourseRepository courseRepository;

    @Mock
    private DepartmentRepository departmentRepository;

    @InjectMocks
    private CourseService courseService;

    private Course testCourse;
    private Department testDepartment;

    @BeforeEach
    void setUp() {
        testDepartment = new Department();
        testDepartment.setId(1L);
        testDepartment.setName("Computer Science");
        testDepartment.setCode("CS");

        testCourse = new Course();
        testCourse.setId(1L);
        testCourse.setName("Data Structures");
        testCourse.setCode("CS201");
        testCourse.setCredits(4);
        testCourse.setDescription("Introduction to Data Structures");
        testCourse.setDepartment(testDepartment);
    }

    /**
     * Test 1: Verify successful course creation.
     */
    @Test
    void testCreateCourse() {
        Course newCourse = new Course();
        newCourse.setName("Algorithms");
        newCourse.setCode("CS301");
        newCourse.setCredits(4);
        newCourse.setDepartment(testDepartment);

        when(courseRepository.existsByCode("CS301")).thenReturn(false);
        when(departmentRepository.findById(1L)).thenReturn(Optional.of(testDepartment));
        when(courseRepository.save(any(Course.class))).thenAnswer(invocation -> {
            Course c = invocation.getArgument(0);
            c.setId(1L);
            return c;
        });

        Course created = courseService.createCourse(newCourse);

        assertNotNull(created);
        assertEquals("Algorithms", created.getName());
        assertEquals("CS301", created.getCode());
        assertEquals(4, created.getCredits());
        verify(courseRepository).save(any(Course.class));
    }

    /**
     * Test 2: Verify getting all courses returns list.
     */
    @Test
    void testGetAllCourses() {
        when(courseRepository.findAll()).thenReturn(List.of(testCourse));

        List<Course> courses = courseService.getAllCourses();

        assertNotNull(courses);
        assertEquals(1, courses.size());
        assertEquals("Data Structures", courses.get(0).getName());
    }

    /**
     * Test 3: Verify getting course by ID returns course when exists.
     */
    @Test
    void testGetCourseById() {
        when(courseRepository.findById(1L)).thenReturn(Optional.of(testCourse));

        Course found = courseService.getCourseById(1L);

        assertNotNull(found);
        assertEquals(1L, found.getId());
        assertEquals("Data Structures", found.getName());
    }

    /**
     * Test 4: Verify course update works correctly.
     */
    @Test
    void testUpdateCourse() {
        Course updatedCourse = new Course();
        updatedCourse.setName("Advanced Data Structures");
        updatedCourse.setCode("CS201");
        updatedCourse.setCredits(3);
        updatedCourse.setDescription("Advanced topics in Data Structures");

        when(courseRepository.findById(1L)).thenReturn(Optional.of(testCourse));
        when(courseRepository.save(any(Course.class))).thenAnswer(invocation -> invocation.getArgument(0));

        Course result = courseService.updateCourse(1L, updatedCourse);

        assertEquals("Advanced Data Structures", result.getName());
        assertEquals(3, result.getCredits());
    }

    /**
     * Test 5: Verify course deletion works correctly.
     */
    @Test
    void testDeleteCourse() {
        when(courseRepository.findById(1L)).thenReturn(Optional.of(testCourse));
        doNothing().when(courseRepository).delete(testCourse);

        courseService.deleteCourse(1L);

        verify(courseRepository).delete(testCourse);
    }
}

